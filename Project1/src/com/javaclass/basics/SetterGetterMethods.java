package com.javaclass.basics;

public class SetterGetterMethods {
	private int age;
	private String firstName;
	public void setAge(int ag) {
		this.age = ag;
	}
	public void setFirstName(String name) {
		this.firstName = name;
	}
	public int getAge() {
		return this.age;
	}
	public String getFirstName() {
		return this.firstName;
	}

	

}
